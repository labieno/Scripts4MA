# Hashing APIs with CRC32
import os, pefile, zlib
# Get API names from known DLLs

path = "C:\\Windows\\System32\\"
DLLs = ["kernel32.dll", "ntdll.dll", "wininet.dll"]
with open("API_hashes.csv", "w") as apihashes:
    for dll in DLLs:
        filename = os.path.join(path, dll)
        pe = pefile.PE(filename)
        d = [pefile.DIRECTORY_ENTRY["IMAGE_DIRECTORY_ENTRY_EXPORT"]]
        pe.parse_data_directories(directories=d)
        
        exports = [e.name for e in pe.DIRECTORY_ENTRY_EXPORT.symbols]
        for export in exports:
            if export != None:
                apihashes.write(export.decode("utf-8") + "," + hex(zlib.crc32(export)) + "\n")

